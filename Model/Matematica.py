

class OperacoesMatematica:
    """Classe para realizar operações básicas de adição e subtração."""

    def somar(self,n1:int, n2:int):
        """
        Está função soma dois números inteiros.

        Parâmetros:
        n1 (int): Recebe um numero inteiro
        n2 (int): Recebe um numero inteiro

        Retorna:
        int: A soma de dois números inteiros.
        """
        return n1 + n2
    

    def subtrair(self,n1:int, n2:int):
        """
        Está função soma dois números inteiros.

        Parâmetros:
        n1 (int): Recebe um numero inteiro
        n2 (int): Recebe um numero inteiro

        Retorna:
        int: A subtração de dois números inteiros.
        """

        return n1 - n2